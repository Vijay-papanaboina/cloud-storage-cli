package util

import (
	"encoding/json"
	"fmt"
	"os"
)

// OutputJSON outputs data as formatted JSON to stdout
func OutputJSON(data interface{}) error {
	encoder := json.NewEncoder(os.Stdout)
	encoder.SetIndent("", "  ")
	if err := encoder.Encode(data); err != nil {
		return fmt.Errorf("failed to encode JSON: %w", err)
	}
	return nil
}

